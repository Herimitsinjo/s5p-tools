def printRed(string): print("\033[91m {}\033[00m" .format(string))


def printGreen(string): print("\033[92m {}\033[00m" .format(string))


def printYellow(string): print("\033[93m {}\033[00m" .format(string))


def printLightPurple(string): print("\033[94m {}\033[00m" .format(string))


def printPurple(string): print("\033[95m {}\033[00m" .format(string))


def printCyan(string): print("\033[96m {}\033[00m" .format(string))


def printLightGray(string): print("\033[97m {}\033[00m" .format(string))


def printBlack(string): print("\033[98m {}\033[00m" .format(string))


def printBold(string): print('\033[1m{}\033[0m'.format(string))
